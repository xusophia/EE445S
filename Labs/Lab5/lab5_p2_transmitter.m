%% combine the header and data to prepare for transmission
x = [header,data];

%% Upsample and apply the pulse shaping filter
fs = 48000;
f0 = 5000;
alpha = 1;
L = 20;
h = rcosdesign(alpha,8,L,'normal');
y = conv(h,upsample(x,L));

%% Modulate the signal to prepare for transmission
t = 1:length(y);
carrier = cos(t*2*pi*f0/fs); % carrier signal
TX = y.*carrier; % upconversion

save TX TX;
for i_count = 10:-1:1; disp(i_count); pause(1); end; soundsc(TX,fs);
